			<h3>PRODUÇÃO AGRÍCOLA E AS ABELHAS</h3>
			<p>
				A agricultura e a polinização andam juntas. Ampliar a produção e melhorar a qualidade dos frutos são benefícios direto obtidos pelo manejo da polinização. Por isto, uma agricultura sustentável deve ter um olhar atento à importância de preservar e impulsionar as atividades de polinização, entendendo esta atividade como estratégica na cadeia produtiva agrícola.
			</p>
			<p>
				Boas práticas para impulsionar a polinização:
			</p>
			<ol>
			  <li>Conservar áreas naturais.</li>
			  <li>Recuperar a vegetação nativa utilizando plantas que atraem e mantêm os polinizadores.</li>
			  <li>Manter a vegetação nativa próxima à área de cultivo.</li>
			  <li>Manter plantas atrativas nas proximidades.</li>
			  <li>Manter e manejar as abelhas próximas às áreas de cultivo.</li>
			  <li>Manter a matéria orgânica no solo para a criação de ninhos.</li>
			  <li>Reduzir e, quando possível, eliminar o uso de agrotóxicos.</li>
			  <li>Não aplicar defensivos nos horários de visita dos polinizadores ao cultivo, geralmente pela manhã.</li>
			  <li> Criar um canal direto de contato com os apicultores e meliponicultores de sua região, para, conforme a lei, informá-los sobre atividades de pulverização aérea.
			  </li>
			</ol>
